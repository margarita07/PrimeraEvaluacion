package com.primeraevaluacionMargarita.primeraevaluacion;

public class Estudiante {
    String Nombres;
    String Codigo;
    String Materia;
    Double Nota1;
    Double Nota2;
    Double Nota3;
    Double Promedio;

    public Estudiante(String nombres, String codigo, String materia, Double nota1, Double nota2, Double nota3, Double promedio) {
        Nombres = nombres;
        Codigo = codigo;
        Materia = materia;
        Nota1 = nota1;
        Nota2 = nota2;
        Nota3 = nota3;
        Promedio = promedio;
    }
}
